#Імпортується бібліотека math
import math
#Об'являється змінна x де значення вводиться через консоль та конвертується в тип float
x = float(input("x = "))
#Переводиться x в радіанну міру
x *= math.pi/180
#Об'являється змінна с де значення вводиться через консоль та конвертується в тип float
c = float(input("c = "))
#Обчислюється g по формулі : g = (sin(x) + c) ^ (1/3)/ c * e ^ (x), та виводиться результат в консоль
print("g = " + str(math.pow(math.sin(x)+c, 1/3) / (c * math.exp(x))))